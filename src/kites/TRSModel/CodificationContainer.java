package kites.TRSModel;

/**
 * Container used in the codification process to store two values as the value in a map.
 * 
 * @author sarek
 */
public class CodificationContainer {
	public int arity;
	public int number;
	
	public CodificationContainer(int arity, int number) {
		this.arity = arity;
		this.number = number;
	}
}
